
// Métodos úteis para usar com strings
console.log("javascript".length)            //  = 10
console.log("javascript".charAt(4))         //  = "s"
console.log("javascript".concat(" dev"))    //  = javascript dev
console.log("javascript".endsWith("t"))     //  = true
console.log("javascript".startsWith("ja"))  //  = true
console.log("javascript".includes("t"))     //  = true
console.log("javascript".indexOf("c"))      //  = 5
console.log("javascript".search("r"))       //  = 6
console.log("javascript".slice(0, 4))       //  = java
console.log("javascript".split(""))         //  = ['j', 'a', 'v', 'a', 's', 'c', 'r', 'i', 'p', 't']
console.log("javascript".substring(4, 6))   //  = "sc"
console.log("JAVASCRIPT".toLowerCase())     //  = "javascript"
console.log("javascript".toUpperCase())     //  = "JAVASCRIPT"
console.log("   javascript".trim())         //  = "javascript"
